const removeFromArray = function(arr, ...remElements) {
    if (!Array.isArray(arr)) {
        return [];
    }
    for (let remEl of remElements) {
        if (arr.includes(remEl)) {
            arr.splice(arr.indexOf(remEl), 1);
        }
    }
    return arr;
};

